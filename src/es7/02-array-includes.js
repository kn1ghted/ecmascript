// INCLUDES method on arrays
let numbersArray = [1,2,3,4,5,6,7,8,9];
console.log(numbersArray.includes(10));
console.log(numbersArray.includes(7));

// Not case sensitive for string values
let stringsArray = ['Item', 'Car', 'Ball', 'FOOD'];
console.log(stringsArray.includes('Item'));
console.log(stringsArray.includes('food'));