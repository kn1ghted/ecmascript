let hello = 'Hello';
let world = 'World';
let phrase = hello + ' ' + world + '!';
console.log(phrase);

// Template literals
let templatePhrase = `${hello} ${world}!`;
console.log(templatePhrase);

// Multiline string
let line = 'This is a text in a line \n'+'This is a second line';
let templateLine = `This is a template line
This is a new template line`;
console.log(line);
console.log(templateLine);